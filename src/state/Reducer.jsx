function reduce(state, action) {
    switch (action.type){
        case 'add-category':
            return state
        case 'delete-category':
            return state
        case 'add-todo':
            return state
        case 'delete-todo':
            return state
        case 'edit-todo':
            return state
        case 'get-category':
            return state        
    }
}